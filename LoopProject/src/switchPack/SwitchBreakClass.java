 package switchPack;

public class SwitchBreakClass {

	public static void main(String[] args) {
		int month=12;
		String Month;
		
		switch(month) {
		case 1:Month="Jan";
		break;
		case 2:Month="Feb";
		break;
		case 3:Month="March";
		break;
		case 4:Month="Apl";
		break;
		case 5:Month="May";
		break;
		case 6:Month="Jun";
		break;
		case 7:Month="Jul";
		break;
		case 8:Month="Aug";
		break;
		case 9:Month="Sep";
		break;
		case 10:Month="Oct";
		break;
		case 11:Month="Nov";
		break;
		case 12:Month="Dec";
		break;
		default:Month="Unknown";
		break;
		
		
		
		}
		System.out.println(Month);

	}

}
